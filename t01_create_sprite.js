let sprite;
/*******************************************************/
// setup()
/*******************************************************/
function setup() {
  console.log("setup");
  new Canvas(250, 100);

  sprite = new Sprite();
  sprite.width = 50;
  sprite.height = 50;
  sprite.color = 'white';
}
	
/*******************************************************/
// draw()
/*******************************************************/
function draw() {
  background('navy');
  sprite.x += 4;
}

